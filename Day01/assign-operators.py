"""
Operatory przypisania
"""
x = 10
postep = 3

x = 10; x+=postep   # x = x + postep
print(" x+= = ", x)

x = 10; x-=postep   # x = x - postep
print(" x-= = ",x)

x = 10; x*=postep   # x = x * postep
print(" x*= = ",x)

x = 10; x/=postep   # x = x / postep
print(" x/= = ",x)

x = 10; x%=postep   # x = x % postep
print(" x%= = ",x)

x = 10; x//=postep   # x = x // postep
print(" x//= = ",x)

