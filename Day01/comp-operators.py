
"""
Operatory porównania
"""
x = 10
y = 6.5

print("x=",x," y=",y)
print(" x = y ? = ", x==y)          # czy równy
print(" x <> y ? = ", x!=y)         # czy różny
print(" x > y ? =", x>y)            # czy x > y
print(" x < y ? = ", x<y)           # czy x < y
print(" x >= y ? =", x>=y)          # czy x>= y
print(" x <= y ? =", x<=y)          # czy x<=y

